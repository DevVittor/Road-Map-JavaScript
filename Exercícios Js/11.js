/*Altere o programa anterior para mostrar no final a soma dos números.*/

let numInt01 = parseInt(prompt('Digite 1º número inteiro: '));
let numInt02 = parseInt(prompt('Digite 2º número inteiro: '));

let loop = numInt02;
while(loop <= numInt02){
    var soma = numInt01 + numInt02;
    loop++;
}
document.write(soma);